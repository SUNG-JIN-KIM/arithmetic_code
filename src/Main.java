public class Main {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;

        int sum = a + b;
        System.out.println("덧셈: " + sum);

        int difference = a - b;
        System.out.println("뺄셈: " + difference);

        int product = a * b;
        System.out.println("곱셈: " + product);

        int quotient = a / b;
        System.out.println("나눗셈: " + quotient);

        int remainder = a % b;
        System.out.println("나머지: " + remainder);
    }
}